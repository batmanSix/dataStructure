// 数据结构算法 数组

// 程序设计的实质 等于算法 + 结构

// 数据 = 符号  数据元素：数据基本单位

// 简单的数据可以用变量或者数组给存起来


// 数据结构作用：阐述关系

// 集合结构： 数据元素属于同一集合，他们之间的关系，他们共同属于一个集合

// 线性结构: 除了第一个和最后一个数据元素之外，其他元素都是首尾相接，
// （1）必须存在第一个元素
// （2）必须存在最后一个元素
// （3）除第一个元素外其他数据均有一个唯一的后续
// （4）除第一个元素外其他元素均有一个唯一的前驱
//  例子： 数组，栈，队列

// 树形结构： 数据元素是一对多的关系

// 图形结构： 数据元素是多对多的关系

// 物理结构:  数据元素存储到存储器中，内存而言：数据的存储结构应该正确的反应元素之间的逻辑关系，
// 顺序存储，链式存储

